package com.gmail.bahlegaji01.payroll;

/** Represents an address of an employee
 * @author Doug Lowe
 * @author www.LoweWriter.com
 * @version 1.5
 * @since 1.0
 */
public class Address implements Cloneable {
    /** Instance variable: the street of the address
     * type of String
     */
    public String street;

    /** Instance variable: the city of the address
     * type of String
     */
    public String city;

    /** Instance variable: the state of the address
     * type of String
     */
    public String state;

    /** Instance variable: the zipcode of the address
     * type of String
     */
    public String zipCode;
}
